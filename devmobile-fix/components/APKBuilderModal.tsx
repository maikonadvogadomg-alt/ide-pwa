function sanitize(s: string) {
  return s.toLowerCase().replace(/\s+/g, "-").replace(/[^a-z0-9-]/g, "");
}
function toPackageId(owner: string, repo: string) {
  const o = (owner || "com").replace(/[^a-z0-9]/gi, "").toLowerCase().slice(0, 20) || "com";
  const r = (repo || "app").replace(/[^a-z0-9]/g, "").slice(0, 20) || "app";
  return `com.${o}.${r}`;
}
function parseGithubUrl(input: string): { owner: string; repo: string } | null {
  const clean = input.trim().replace(/\.git$/, "");
  const m =
    clean.match(/github\.com[/:]([\w.-]+)\/([\w.-]+)/) ||
    clean.match(/^([\w.-]+)\/([\w.-]+)$/);
  if (m) return { owner: m[1], repo: m[2] };
  return null;
}
function extractColor(html: string): string {
  const m = html.match(/theme-color["']?\s+content=["']([#\w]+)/i) ||
            html.match(/background[_-]?color["']?\s*[:=]\s*["']([#\w]+)/i);
  return m?.[1] || "#0d1117";
}

